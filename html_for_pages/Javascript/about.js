function SwapCont(type){

    
    if(type == "we"){
        document.getElementById("weare").style.display = "block";
        document.getElementById("miss").style.display = "none";
    }
    if(type == "miss"){
        document.getElementById("miss").style.display = "block";
        document.getElementById("weare").style.display = "none";
    }
}
